package modul02.Latihan2;

public class CekGanjilGenap {
    public static void main(String[] args) {
        int angka = 7;

        if (angka % 2 == 0) {
            System.out.println("Angka " + angka + " adalah genap.");
        } else {
            System.out.println("Angka " + angka + " adalah ganjil.");
        }
    }
}
